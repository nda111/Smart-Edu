package com.gachon.smartedu.Item;

public class UserItem {
    public String email;
    public String id_num;
    public String user_name;
    public String uid;
    public String user_position;

    public UserItem(String email, String id_num, String user_name, String uid, String user_position){
        this.email = email;
        this.id_num = id_num;
        this.user_name = user_name;
        this.uid = uid;
        this.user_position = user_position;
    }
}
